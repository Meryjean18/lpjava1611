    private void tbListaEmpleadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbListaEmpleadosMouseClicked
        // TODO add your handling code here:
        int fila;
        fila = tbListaEmpleados.getSelectedRow();
              
        int id;
        String nombre, ape_pat, ape_mat;
        LocalDate fnacimiento;
        double sueldo;
        
        if (fila>=0){
            //columnas: ID - NOMBRE - APE_PAT - APE_MAT - EDAD - SUELDO
            //POSICIONES: 0 - 1 - 2 - 3 - 4 - 5
            id = Integer.parseInt(tbListaEmpleados.getValueAt(fila, 0) + "");
            nombre = tbListaEmpleados.getValueAt(fila, 1) + "";
            ape_pat= tbListaEmpleados.getValueAt(fila, 2) + "";
            ape_mat= tbListaEmpleados.getValueAt(fila, 3) + "";
            fnacimiento = LocalDate.parse(tbListaEmpleados.getValueAt(fila, 4) + "");
            sueldo= Double.parseDouble(tbListaEmpleados.getValueAt(fila, 5) + "");
            txtId.setText(id + "");
            txtNombre.setText(nombre);            
            txtApePat.setText(ape_pat);
            txtApeMat.setText(ape_mat);
            dtpFechaNacimiento.getModel().setDate(
                    fnacimiento.getYear(),
                    fnacimiento.getMonthValue()-1,
                    fnacimiento.getDayOfMonth());            
            dtpFechaNacimiento.getModel().setSelected(true);
            txtSueldo.setText(sueldo + "");           
            
        }
    }//GEN-LAST:event_tbListaEmpleadosMouseClicked

