    Empleado getEmpleado(){
        
        //construir la fecha a partir del jdatepich¬øker
        int anio,mes, dia;
        anio = dtpFechaNacimiento.getModel().getYear();
        mes = dtpFechaNacimiento.getModel().getMonth();
        dia = dtpFechaNacimiento.getModel().getDay();
                
        
        return new Empleado(
                Integer.parseInt(txtId.getText()),
                txtNombre.getText(),
                txtApePat.getText(),
                txtApeMat.getText(),
                LocalDate.of(anio, mes,dia),
                Double.parseDouble(txtSueldo.getText())             
                
        );
        
    }

    
